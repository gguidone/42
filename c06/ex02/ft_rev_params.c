#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	stampa(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = argc - 1;
	while (i > 0)
	{
		stampa(argv[i]);
		putchar('\n');
		i--;
	}
	return (0);
}
