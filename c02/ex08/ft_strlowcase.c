char	*ft_strlowcase(char *str)
{
	int	cont;

	cont = 0;
	while(*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str + 32;
		cont++;
		str++;
	}
	return (str);
}
