char	*ft_strupcase(char *str)
{
	int	cont;

	cont = 0;
	while (*str != '\0')
	{
		if ( *str >=  'a' && *str <= 'z')
			*str = *str - 32;
		str++;
		cont++;
	}
	return (str);
}
