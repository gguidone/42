int	ft_str_is_alpha(char *str)
{
	while( *str != '\0')
	{
		if(*str < 'a' || *str > 'z')
			return (0);
		else if(*str < 'A' || *str > 'Z')
			return (0);
		str++;
	}
	return (1);
}
